package matrix;

//Class that represents one point in the matrix.
class Point {
	public int x;
	public int y;

  /**
   * Constructor, assigning parameters to class properties.
   * @param x X coordinate of the point.
   * @param y Y coordinate of the point.
   */
	Point(int x, int y) {
		this.x = x;
		this.y = y;
  }
}

